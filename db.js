// DB Operations Layer
const db = {
    init() {
        const CURRENT_DB_VERSION = "v12";
        const isInitialized = localStorage.getItem("vellore_tours_db_initialized");
        const currentVersion = localStorage.getItem("vellore_tours_db_version");

        if (!isInitialized || currentVersion !== CURRENT_DB_VERSION) {
            localStorage.setItem("tour_operators", JSON.stringify(DEFAULT_TOUR_OPERATORS));
            localStorage.setItem("taxi_services", JSON.stringify(DEFAULT_TAXI_SERVICES));
            localStorage.setItem("tourist_places", JSON.stringify(DEFAULT_TOURIST_PLACES));
            localStorage.setItem("atms", JSON.stringify(DEFAULT_ATMS));
            localStorage.setItem("hotels", JSON.stringify(DEFAULT_HOTELS));
            localStorage.setItem("restaurants", JSON.stringify(DEFAULT_RESTAURANTS));
            localStorage.setItem("news", JSON.stringify(DEFAULT_NEWS));
            
            // Keep user-created bookings and messages if they exist
            if (!localStorage.getItem("bookings")) {
                localStorage.setItem("bookings", JSON.stringify([]));
            }
            if (!localStorage.getItem("contact_messages")) {
                localStorage.setItem("contact_messages", JSON.stringify([]));
            }
            
            localStorage.setItem("vellore_tours_db_initialized", "true");
            localStorage.setItem("vellore_tours_db_version", CURRENT_DB_VERSION);
        }
    },

    getTable(tableName) {
        this.init();
        return JSON.parse(localStorage.getItem(tableName)) || [];
    },

    saveTable(tableName, data) {
        localStorage.setItem(tableName, JSON.stringify(data));
    },

    // Generic CRUD Operations
    getAll(table) {
        return this.getTable(table);
    },

    getById(table, id) {
        const items = this.getTable(table);
        return items.find(item => item.id === id) || null;
    },

    add(table, item) {
        const items = this.getTable(table);
        item.id = `${table.substring(0, 2)}-${Date.now()}`;
        items.unshift(item);
        this.saveTable(table, items);
        return item;
    },

    update(table, id, updatedItem) {
        const items = this.getTable(table);
        const index = items.findIndex(item => item.id === id);
        if (index !== -1) {
            items[index] = { ...items[index], ...updatedItem, id };
            this.saveTable(table, items);
            return true;
        }
        return false;
    },

    delete(table, id) {
        const items = this.getTable(table);
        const filtered = items.filter(item => item.id !== id);
        if (items.length !== filtered.length) {
            this.saveTable(table, filtered);
            return true;
        }
        return false;
    }
};

// Initialize DB immediately
db.init();
window.VelloreDB = db;
